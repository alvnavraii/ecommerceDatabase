    UPDATED_BY VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_ADDRESSES PRIMARY KEY (ID),
    CONSTRAINT FK_ADDRESSES_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
